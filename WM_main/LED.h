#include <stdint.h>
#pragma once

/*******************************************************************************
@Class definitions (global)
*******************************************************************************/
class LED
{
public:
    /*******************************************************************************
    @Description   Public constructor.

    --------------------------------------------------------------------------------
    @Returns       Nothing.

    --------------------------------------------------------------------------------
    @Parameters    None.
    *******************************************************************************/
    LED(void);


    /*******************************************************************************
    @Description   Public constructor. Set the desired pins as OUTPUT.

    --------------------------------------------------------------------------------
    @Returns       Nothing.

    --------------------------------------------------------------------------------
    @Parameters    R_pin_u8 - pin number
                   G_pin_u8 - pin number
                   B_pin_u8 - pin number
    *******************************************************************************/
    LED(uint8_t R_pin_u8, uint8_t G_pin_u8, uint8_t B_pin_u8);

    void setColorGreen_v();
    void setColorRed_v();
    void setColorBlue_v();
    void setColor_v(uint8_t R_color_u8, uint8_t G_color_u8, uint8_t B_color_u8);
    /*******************************************************************************
    @Description   Public destructor.

    --------------------------------------------------------------------------------
    @Returns       Nothing.

    --------------------------------------------------------------------------------
    @Parameters    None.
    *******************************************************************************/
    ~LED(void);
private:
    uint8_t R_pin_u8;
    uint8_t G_pin_u8;
    uint8_t B_pin_u8;
};

